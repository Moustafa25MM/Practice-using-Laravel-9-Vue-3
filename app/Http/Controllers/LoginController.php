    {
        $credentials = $request->validate([
            'email' => ['required', 'email'],
            'password' => ['required'],
               ]);

               if (Auth::attempt($credentials))
               {
                  return response()->json([ 'status' => true ,
                                            'message' => "You Login Successfully",
                                            'userId' => Auth::id()
               ]);
               }
                   return response()->json(['status' => false ,
